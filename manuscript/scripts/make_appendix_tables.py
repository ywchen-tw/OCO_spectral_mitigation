def table(colspec, header, rows, caption, label, notes=None, size="\\small",
          placement="t"):
    lines = [f"\\begin{{table}}[{placement}]",
             f"\\caption{{{caption}}}",
             f"\\label{{{label}}}", size,
             f"\\begin{{tabular}}{{{colspec}}}", TOPH,
             header + " \\\\", MIDH,
             *[r if r in (MIDH,) else r + " \\\\" for r in rows],
             BOTH, "\\end{tabular}"]
    if notes:
        lines.append(f"\\\\[3pt]\\parbox{{\\linewidth}}{{\\footnotesize {notes}}}")
    lines.append("\\end{table}")
    return "\n".join(lines) + "\n"


def longtable(colspec, header, rows, caption, label, notes=None,
              size="\\footnotesize", colsep=None):
    head = header + " \\\\"
    lines = ["\\begingroup" + size]
    if colsep:
        lines.append(f"\\setlength{{\\tabcolsep}}{{{colsep}}}")
    lines += [
             f"\\begin{{longtable}}{{{colspec}}}",
             f"\\caption{{{caption}}}",
             f"\\label{{{label}}}\\\\",
             TOPH, head, MIDH, "\\endfirsthead",
             "\\multicolumn{%d}{l}{\\textit{(continued)}}\\\\" % len(header.split("&")),
             TOPH, head, MIDH, "\\endhead",
             BOTH, "\\endfoot",
             BOTH]
    if notes:
        lines.append("\\multicolumn{%d}{p{0.92\\linewidth}}{\\footnotesize %s}\\\\"
                     % (len(header.split("&")), notes))
    lines.append("\\endlastfoot")
    lines += [r if r in (MIDH,) else r + " \\\\" for r in rows]
    lines += ["\\end{longtable}", "\\endgroup"]
    return "\n".join(lines) + "\n"


