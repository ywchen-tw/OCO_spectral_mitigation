def tabE1():
    w = pd.read_csv(DEBASE / "failure_modes" / "worseners_r100km.csv")
    w = w[(w.tree == "atrain") & w.worse_bias].sort_values(
        "d_absbias", ascending=False)

    def cat(r):
        flip = np.sign(r.bias_after) != np.sign(r.bias_before)
        if abs(r.bias_before) <= 0.3:
            return "near-zero start"
        return "overshoot (sign flip)" if flip else "amplified"

    rows = []
    for _, r in w.iterrows():
        rows.append(
            f"{r.site} & {r.date} & {int(r.n_oco):,}".replace(",", "\\,")
            + f" & {r.cld_dist_mu:.1f} & {r.bias_before:+.2f} & "
              f"{r.bias_after:+.2f} & {r.d_absbias:+.2f} & "
              f"{r.d_rmse:+.2f} & {cat(r)}")
    n_rmse_ok = int((w.d_rmse < 0).sum())
    note = ("Worsened station-days: $|$bias$_{\\mathrm{DE}}| > "
            "|$bias$_{\\mathrm{B11}}|$ against the AK-harmonized reference "
            f"(median $\\Delta|$bias$| = {w.d_absbias.median():+.2f}$\\,ppm). "
            "Category: near-zero start = initial $|$bias$| \\le 0.3$\\,ppm "
            "(below the sign-flip floor of the within-overpass scatter); "
            "overshoot = the correction crosses zero; amplified = the "
            "residual grows with unchanged sign. The per-footprint RMSE "
            f"still improves in {n_rmse_ok} of {len(w)} of these cases "
            "($\\Delta$RMSE $<0$), consistent with a common-mode residual "
            "rather than a per-footprint mis-correction (Sect.~4.4).")
    cap = ("All station-days whose mean $|$bias$|$ worsens after correction "
           "(primary 100\\,km comparison, all quality flags), with the "
           "arithmetic category of the worsening.")
    return longtable("llrrrrrrl",
                     "Site & Date & $n$ & $\\bar{d}_{\\mathrm{cld}}$ & "
                     "bias$_{\\mathrm{B11}}$ & bias$_{\\mathrm{DE}}$ & "
                     "$\\Delta|$bias$|$ & $\\Delta$RMSE & Category",
                     rows, cap, "tab:worsening-cases", notes=note)


