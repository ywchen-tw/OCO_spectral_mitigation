def tabD3():
    def ci(m, lo, hi):
        return f"${m:+.2f}$ [{lo:+.2f}, {hi:+.2f}]"

    def pfmt(p):
        if p >= 1e-4:
            return f"{p:.4f}"
        mant, expo = f"{p:.1e}".split("e")
        return f"${mant}\\times10^{{{int(expo)}}}$"

    rows = []
    for radius, rlab in [("r100km", "100\\,km"), ("r50km", "50\\,km")]:
        sig = pd.read_csv(ATRAIN / f"tccon_significance_{radius}.csv")
        rows.append(f"\\multicolumn{{7}}{{l}}{{\\textit{{Radius {rlab}, "
                    "$\\pm$60\\,min}}")
