def dl_pool(b, u):
    """DerSimonian–Laird random-effects pool. Returns (mu, se, tau, I2, n).

    Cases without a finite (b, u) pair are dropped, matching the report md
    (e.g. the one r50 station-day whose budget could not be evaluated).
    """
    b, u = np.asarray(b, float), np.asarray(u, float)
    ok = np.isfinite(b) & np.isfinite(u)
    b, u = b[ok], u[ok]
    w = 1.0 / u**2
    mu_fe = np.sum(w * b) / np.sum(w)
    q = np.sum(w * (b - mu_fe)**2)
    s = len(b)
    tau2 = max(0.0, (q - (s - 1)) / (np.sum(w) - np.sum(w**2) / np.sum(w)))
    ws = 1.0 / (u**2 + tau2)
    mu = np.sum(ws * b) / np.sum(ws)
    se = np.sum(ws) ** -0.5
    i2 = max(0.0, (q - (s - 1)) / q) if q > 0 else 0.0
    return mu, se, np.sqrt(tau2), i2, s


